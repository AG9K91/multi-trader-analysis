function computeADFStat(series) {
  // Delta y_t = gamma * y_{t-1} + sum delta_i * Delta y_{t-i} + e_t
  const dy = [];
  const lagged = [];
  for (let i = 1; i < series.length; i++) {
    dy.push(series[i] - series[i-1]);
    lagged.push(series[i-1]);
  }
  const xm = mean(lagged), ym = mean(dy);
  let num = 0, den = 0;
  for (let i = 0; i < dy.length; i++) {
    num += (lagged[i] - xm) * (dy[i] - ym);
    den += (lagged[i] - xm) ** 2;
  }
  const gamma = num / den;
  const resid = dy.map((y,i) => y - ym - gamma * (lagged[i] - xm));
  const rss = resid.reduce((s,r) => s + r*r, 0);
  const se_gamma = Math.sqrt(rss / (dy.length - 2) / den);
  return gamma / se_gamma;
}

