// ADF 检验 (简化版: Dickey-Fuller test)
function adfTest(series) {
  // Implement simplified ADF test
  const n = series.length;
  const dy = [];
  const y_lag = [];
  for (let i = 1; i < n; i++) {
    dy.push(series[i] - series[i-1]);
    y_lag.push(series[i-1]);
  }

  // OLS: dy = alpha + beta * y_lag
  const xMean = mean(y_lag);
  const yMean = mean(dy);
  let num = 0, den = 0;
  for (let i = 0; i < dy.length; i++) {
    num += (y_lag[i] - xMean) * (dy[i] - yMean);
    den += (y_lag[i] - xMean) ** 2;
  }
  const beta = num / den;
  const alpha = yMean - beta * xMean;

  // Standard error
  const residuals = dy.map((y,i) => y - alpha - beta * y_lag[i]);
  const rss = residuals.reduce((s,r) => s + r*r, 0);
  const se = Math.sqrt(rss / (dy.length - 2) / den);
  const tStat = beta / se;

  return { tStat, beta, alpha, n: dy.length };
}

